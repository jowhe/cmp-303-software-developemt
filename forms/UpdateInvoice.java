    private void btnGenerateInvoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateInvoiceActionPerformed
        // Get the local date of pay and date of next pay and set a java.sql.Timestamp for each.
        java.time.LocalDateTime dtp = dateOfPay.getDateTimeStrict();
        java.time.LocalDateTime dtnp = dateOfNext.getDateTimeStrict();
        java.sql.Timestamp datePay = java.sql.Timestamp.valueOf(dtp);
        java.sql.Timestamp dateNextPay = java.sql.Timestamp.valueOf(dtnp);
        
        String status = "Unpaid";
        if(comboFreq.getSelectedItem().equals("One-off")){
            status = "Paid";
        }
        
        // Values to insert into the database.
        Object[] values = new Object[]{
            jobs.get((String) comboCustomers.getSelectedItem()),
            customers.get((String) comboCustomers.getSelectedItem()),
            (int) totalCost,
            0,
            (String) comboFreq.getSelectedItem(),
            methods.get((String) comboMethods.getSelectedItem()),
            datePay,
            dateNextPay,
            status,
            selectedInvoice
        };
        
        if(DatabaseManager.Update("UPDATE invoices SET job_id = ?, customer_id = ?, invoice_owed = ?, invoice_paid = ?, invoice_freq = ?, invoice_method = ?, invoice_pay_date = ?, invoice_next_pay_date = ?, invoice_status = ? WHERE invoice_id = ?", values)){
            // If the database inserts successfully, repopulate the InvoiceView and dispose of the form.
            InvoiceView iv = new InvoiceView(USER, VHANDLER);
            VHANDLER.changeView(iv, PARENT);
            iv.populate();
            this.dispose();
            new Log(Commands.getDateTime(), "Successfully generated a new invoice.", USER.getId()).send();
        }else{
            new Log(Commands.getDateTime(), "Failed to generate a new invoice.", USER.getId()).send();
        }
        
    }//GEN-LAST:event_btnGenerateInvoiceActionPerformed

    private void comboCustomersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboCustomersActionPerformed
        // If adding customers is in progres do nothing.
        if(addingCustomers){
            this.setSize(this.getWidth(), 201);
            jPanel3.setVisible(false);
            return;
        }
        
        // Once the customers are added, if the user selects an index.
        if(comboCustomers.getSelectedItem() != null){
            // Update the forms size and styling
            this.setSize(this.getWidth(), 500);
            jPanel3.setVisible(true);
            
            // Reset the total cost value
            totalCost = 0.00f;
            
            // Get the tasks for the job from the tasks database.
            java.sql.ResultSet taskRes = DatabaseManager.Query("SELECT i.invoice_id, t.task_description, t.task_cost, c.customer_surname, c.customer_email FROM tasks t JOIN jobs j ON j.job_id = t.job_id JOIN invoices i ON i.job_id = j.job_id JOIN customers c ON j.customer_id = c.customer_id WHERE t.job_id = ?", new Object[]{
                jobs.get((String) comboCustomers.getSelectedItem())
            });
            
            try {
                while(taskRes.next()){
                    // Clear the previous panel.
                    if(panelCostOverview.getComponentCount() >= 5){
                        panelCostOverview.removeAll();
                    }
                    
                    // Add all the new tasks to the panel.
                    InvoiceTask it = new InvoiceTask(taskRes.getString("task_description"), taskRes.getFloat("task_cost"));
                    panelCostOverview.add(it);
                    it.setVisible(true);
                    panelCostOverview.repaint();
                    panelCostOverview.revalidate();
                    
                    // Increase the total cost accordingly.
                    totalCost += (float) taskRes.getFloat("task_cost");
                    selectedInvoice = taskRes.getInt("invoice_id");
                }
            } catch (java.sql.SQLException e) {
                // Display an error message and Log the error.
                FormHandler.openMessageBox(new MessageBox("Error!", e.getMessage() + "\n\nContact System Administrator", javax.swing.JOptionPane.ERROR_MESSAGE));
                new Log(Commands.getDateTime(), e.getMessage(), USER.getId()).send();
            }
            
            totalCostLabel.setText("Total Cost: £" + totalCost);
            dateOfPay.datePicker.setDateToToday();
            dateOfPay.timePicker.setTimeToNow();
            
            dateOfNext.setEnabled(false);
            java.time.LocalDate date = dateOfPay.getDatePicker().getDate();
            java.time.LocalTime time = dateOfPay.getTimePicker().getTime();
            dateOfNext.datePicker.setDate(date);
            dateOfNext.timePicker.setTime(time);
        }
        
        DatabaseManager.Disconnect();
    }//GEN-LAST:event_comboCustomersActionPerformed

    private void comboFreqItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboFreqItemStateChanged
        // Update the date of pay and date of next pay depening on the frequency of the payments.
        if(evt.getStateChange() == java.awt.event.ItemEvent.SELECTED){
            if(comboFreq.getSelectedItem() == "One-off"){
                java.time.LocalDate date = dateOfPay.getDatePicker().getDate();
                java.time.LocalTime time = dateOfPay.getTimePicker().getTime();
                dateOfNext.datePicker.setDate(date);
                dateOfNext.timePicker.setTime(time);
            }
            
            if(comboFreq.getSelectedItem() == "Monthly"){
                java.time.LocalDate date = dateOfPay.getDatePicker().getDate();
                java.time.LocalDate nDate = date.plusMonths(1);
                dateOfNext.datePicker.setDate(nDate);
                dateOfNext.timePicker.setTime(java.time.LocalTime.MIN);
            }
            
            if(comboFreq.getSelectedItem() == "Annually"){
                java.time.LocalDate date = dateOfPay.getDatePicker().getDate();
                java.time.LocalDate nDate = date.plusYears(1);
                dateOfNext.datePicker.setDate(nDate);
                dateOfNext.timePicker.setTime(java.time.LocalTime.MIN);
            }
        }
    }//GEN-LAST:event_comboFreqItemStateChanged

