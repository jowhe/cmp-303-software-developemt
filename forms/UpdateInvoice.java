    /**
     * Update Invoice Window.
     * @param user The user that is logged in.
     * @param viewHandler The view handler from the dashboard.
     * @param panel The panel that the view is contained inside.
     **/
    public UpdateInvoice(User user, ViewHandler viewHandler, javax.swing.JPanel panel) {
        this.USER = user;
        this.VHANDLER = viewHandler;
        this.PARENT = panel;
        
        initComponents();
        
        // Custom Form Styling
        this.setLocation(java.awt.Toolkit.getDefaultToolkit().getScreenSize().width / 2 - (this.getWidth() / 2), java.awt.Toolkit.getDefaultToolkit().getScreenSize().height / 2 - (this.getHeight() / 2));
        java.util.List<javax.swing.JButton> buttons = new java.util.ArrayList<>();
        buttons.add(btnGenerateInvoice);
        StyleSetting.applyButtonStyles(buttons, btnGenerateInvoice.getBackground(), btnGenerateInvoice.getBorder());
        
        // Get the customers and payment methods from the database.
        java.sql.ResultSet customerRes = DatabaseManager.Query("SELECT c.customer_id, c.customer_email, j.job_description, j.job_id FROM customers c JOIN jobs j ON c.customer_id = j.customer_id WHERE c.customer_id IN (SELECT customer_id FROM invoices)");
        java.sql.ResultSet paymentMethods = DatabaseManager.Query("SELECT method_id, method_name FROM payment_methods");
        try {
            // Add the customers and jobs to their HashMaps and insert the customers into the combobox.
            addingCustomers = true;
            while(customerRes.next()){
                comboCustomers.addItem(customerRes.getString("job_description") + " ("+customerRes.getString("customer_email")+")");
                jobs.put(customerRes.getString("job_description") + " ("+customerRes.getString("customer_email")+")", customerRes.getInt("job_id"));
                customers.put(customerRes.getString("job_description") + " ("+customerRes.getString("customer_email")+")", customerRes.getInt("customer_id"));
            }
            // Add the methods to their HashMap and insert the methods into the combobox.
            while(paymentMethods.next()){
                comboMethods.addItem(paymentMethods.getString("method_name"));
                methods.put(paymentMethods.getString("method_name"), paymentMethods.getInt("method_id"));
            }
        } catch (java.sql.SQLException e) {
            // Display an error message and Log the error.
            FormHandler.openMessageBox(new MessageBox("Error!", e.getMessage() + "\n\nContact System Administrator", javax.swing.JOptionPane.ERROR_MESSAGE));
            new Log(Commands.getDateTime(), e.getMessage(), USER.getId()).send();
        }
        
        DatabaseManager.Disconnect();
        comboCustomers.setSelectedIndex(-1);
        addingCustomers = false;
        this.setSize(this.getWidth(), 201);
    }

