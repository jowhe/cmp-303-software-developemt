    private void btnSaveNotesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveNotesActionPerformed
        // TODO add your handling code here:        
        Object[] values = new Object[]{
            inputJobNotes.getText(),
            jobID
        };
        
        // if the update is successful
        if(DatabaseManager.Update("UPDATE jobs SET job_notes = ? WHERE job_id = ?", values)){
            // Repopulate the TaskView.
            TasksView t = new TasksView(USER, VHANDLER);
            VHANDLER.changeView(t, PARENT);
            t.populate(this.jobID);
            
            // Dispose this form, show feedback and insert a log.
            this.dispose();
            FormHandler.openMessageBox(new MessageBox("Success!", "Successfully added a note!", javax.swing.JOptionPane.INFORMATION_MESSAGE));
            new Log(Commands.getDateTime(), "Successfully added a note to job, ID: " + jobID, USER.getId()).send();
        }else{
            new Log(Commands.getDateTime(), "Failed to add a note to job, ID: " + jobID, USER.getId()).send();
        }
    }//GEN-LAST:event_btnSaveNotesActionPerformed

