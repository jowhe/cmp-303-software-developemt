    /**
     * Add Note Window.
     * @param user The user that is logged in.
     * @param viewHandler The view handler from the dashboard.
     * @param panel The panel that the view is contained inside.
     * @param selectedTask The task that is selected.
     */
    public AddNote(User user, ViewHandler viewHandler, javax.swing.JPanel panel, int selectedTask) {
        this.USER = user;
        this.VHANDLER = viewHandler;
        this.PARENT = panel;
                
        initComponents();
        
        // Custom Form styling
        this.setLocation(java.awt.Toolkit.getDefaultToolkit().getScreenSize().width / 2 - (this.getWidth() / 2), java.awt.Toolkit.getDefaultToolkit().getScreenSize().height / 2 - (this.getHeight() / 2));
        java.util.List<javax.swing.JButton> buttons = new java.util.ArrayList<>();
        buttons.add(btnSaveNotes);
        StyleSetting.applyButtonStyles(buttons, btnSaveNotes.getBackground(), btnSaveNotes.getBorder());
        
        // Get the associated job with the given task id.
        java.sql.ResultSet jobsRes = DatabaseManager.Query("SELECT t.job_id, j.job_notes FROM tasks t JOIN jobs j ON t.job_id = j.job_id WHERE task_id = ?", new Object[]{selectedTask});
        try {
            while(jobsRes.next()){
                // Set the jobID and the already created job nots.
                jobID = jobsRes.getInt("job_id");
                inputJobNotes.setText(jobsRes.getString("job_notes"));
            }
        } catch (java.sql.SQLException e) {
            // Display an error and insert a log.
            FormHandler.openMessageBox(new MessageBox("Error!", e.getMessage() + "\n\nContact System Administrator", javax.swing.JOptionPane.ERROR_MESSAGE));
            new Log(Commands.getDateTime(), e.getMessage(), USER.getId()).send();
        }
        
        DatabaseManager.Disconnect();
    }

